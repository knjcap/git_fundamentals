package labs_examples.datatypes_operators.labs;

/**
 * Please demonstrate the use of all arithmetic operators below. These include:
 *
 * addition, subtraction, multiplication, division and modulus
 *
 */
class ArithmeticOperators {

    public static void main(String[] args) {

        int a = 10 + 20;
            System.out.println("10 + 20 = " +a);
        int s = 23 - 9;
            System.out.println("23 - 9 = " +s);
        int m = 12 * 4;
        System.out.println("12 * 4 = " +m);
        int d = 16 / 4;
            System.out.println("16 / 4 = " +d);
        int mod = 14 % 6;
            System.out.println("14 / 6 = 2 remainder "+mod);

    }

}
